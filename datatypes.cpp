#include<bits/stdc++.h>
using namespace std;

int main()
{
    int a=2;
    float b=2.5;
    char ch='c';
    bool ans=0;
    cout<<a<<" ";
    cout<<sizeof(a)<<"\n";
    cout<<b<<" ";
    cout<<sizeof(b)<<"\n";
    cout<<ch<<" ";
    cout<<sizeof(ch)<<"\n";
    cout<<ans<<" ";
    cout<<sizeof(ans)<<"\n";
    long long int d;
    cout<<sizeof(d);
    short int e;
    cout<<sizeof(e);
    return 0;
}